// PickN 从 Library 随机挑 n 条不重复的名言。
// 如果 n >= len(Library),返回 Library 全量乱序拷贝。
// rand 使用全局 source(Go 1.20+ 默认安全),crawler 启动时会被自然地分散种子。
func PickN(n int) []Quote {
	total := len(Library)
	if n <= 0 {
		return nil
	}
	if n > total {
		n = total
	}
	// 用 Perm 取前 n 个索引,避免重复
	idx := rand.Perm(total)[:n]
	out := make([]Quote, 0, n)
	for _, i := range idx {
		out = append(out, Library[i])
	}
	return out
}
